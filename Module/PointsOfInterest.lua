function PointsOfInterest:GetWorkshop(area, skillId)
    area = string.lower(area)
    local jobWorkshop
    for _, v in pairs(self.workshopInfo:Enumerate()) do
        if v["skillid"]:Contains(skillId) then
            local areaWorkShop = v[area]
            jobWorkshop = areaWorkShop:Get(skillId)
            break
        end
    end

    return jobWorkshop
end


